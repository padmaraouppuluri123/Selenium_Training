package com_demosession;

public class DataTypes {
	
	
	public static void main(String[] args) {
		
	
	int age = 25; // 'int' stores whole numbers (e.g., 5, -12)
	float pi = 3.14f; // 'float' stores decimal numbers (less precision)
	double g = 9.81; // 'double' stores larger decimal numbers (more precision)
	char grade = 'A'; // 'char' stores a single character (e.g., 'A', 'b')
	boolean isTrue = true; // 'boolean' stores true or false

	
	System.out.println(age);
	System.out.println(pi);
	System.out.println(g);
	System.out.println(grade);
	System.out.println(isTrue);
	
	
	
	}
}
