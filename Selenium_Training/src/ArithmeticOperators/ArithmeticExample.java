package ArithmeticOperators;

public class ArithmeticExample {

	// Used to perform basic math operations.

	public static void main(String[] args) {
		int a = 10, b = 3;
		System.out.println((a + b));
		System.out.println((a - b));
		System.out.println( (a * b));
		System.out.println((a / b));
		System.out.println((a % b));
	}

}
